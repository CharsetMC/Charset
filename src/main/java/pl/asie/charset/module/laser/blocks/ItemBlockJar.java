package pl.asie.charset.module.laser.blocks;

import net.minecraft.block.Block;
import net.minecraft.client.resources.I18n;
import net.minecraft.client.util.ITooltipFlag;
import net.minecraft.item.ItemStack;
import net.minecraft.util.text.TextFormatting;
import net.minecraft.world.World;
import net.minecraftforge.fml.relauncher.Side;
import net.minecraftforge.fml.relauncher.SideOnly;
import pl.asie.charset.lib.item.ItemBlockBase;
import pl.asie.charset.module.laser.CharsetLaser;

import javax.annotation.Nullable;
import java.util.List;

public class ItemBlockJar extends ItemBlockBase {
	public ItemBlockJar(Block block) {
		super(block);
		setHasSubtypes(true);
	}

	@Override
	@SideOnly(Side.CLIENT)
	public void addInformation(ItemStack stack, @Nullable World worldIn, List<String> tooltip, ITooltipFlag flagIn) {
		if (stack.getItemDamage() > 0) {
			tooltip.add(TextFormatting.ITALIC + I18n.format("tile.charset.light_jar.tip",
					I18n.format(CharsetLaser.LASER_LANG_STRINGS[stack.getItemDamage() & 7])));
		}
	}
}
